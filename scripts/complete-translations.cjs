// Fonction pour traduire une chaîne
function translateString(str, dict) {
  if (typeof str !== 'string') return str;

  // Essayer la correspondance exacte d'abord
  if (dict[str]) return dict[str];

  // Essayer avec des variations de casse
  const strLower = str.toLowerCase();
  for (const [key, value] of Object.entries(dict)) {
    if (key.toLowerCase() === strLower) {
      // Préserver la casse originale si possible
      if (str === str.toUpperCase()) return value.toUpperCase();
      if (str[0] === str[0].toUpperCase()) {
        return value.charAt(0).toUpperCase() + value.slice(1);
      }
      return value;
    }
  }

  // Essayer de remplacer des parties de la chaîne (pour les phrases)
  let result = str;
  // Trier par longueur décroissante pour remplacer les phrases longues d'abord
  const sortedKeys = Object.keys(dict).sort((a, b) => b.length - a.length);

  for (const key of sortedKeys) {
    if (result.includes(key)) {
      result = result.split(key).join(dict[key]);
    }
  }

  return result;
}

// Fonction récursive pour traduire tout l'objet
function translateObject(obj, dict) {
  if (typeof obj === 'string') {
    return translateString(obj, dict);
  }

  if (Array.isArray(obj)) {
    return obj.map(item => translateObject(item, dict));
  }

  if (typeof obj === 'object' && obj !== null) {
    const result = {};
    for (const [key, value] of Object.entries(obj)) {
      result[key] = translateObject(value, dict);
    }
    return result;
  }

  return obj;
}

